num1 = 10 
num2 = 4
print("num1 & num2 are:", num1, num2)
print("num1 | num2 are:", num1 | num2)
print("num1 ^ num2 are:", num1 ^ num2)
print("num1 << num2 are:", num1 << num2)
print("num1 >> num2 are:", num1 >> num2)
print("~num1 are:", ~num1)
print("~num2 are:", ~num2)

