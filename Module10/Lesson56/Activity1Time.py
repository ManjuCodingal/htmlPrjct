#  Constant time complexity is O(1) which means that the time taken by the computer to execute the code will not change with the change in the value of 'n'.

def printnumber(n):
  literation = 0
  print("the total number entered my user is",n)
  literation+=1
  print("total literation done by computer is",literation,"\n")
printnumber(10)
printnumber(200)
print("\n with any 'n' the time taken by the computer will not change")